//public 表示公开的 啪布雷克
// class 表示定义一个类名
// helloworld 表示一个类名


public class HelloWorld{//表示一个公开的类，起名helloworld 这个叫类 里边的就是 类体
	//类体中不允许直接编写java语句（从入口进啊喂！）【除声明变量以外】

	/*
	public 表示公开的
	static表示静态的 丝带dei克
	void表示空 喔爱得
	main表示 方法名
	(String[] args）是一个main方法的形式参数列表
	以下的方法是一个程序的主方法，是程序的执行入口
	是sun公司规定的 固定编写方式
	*/
	 public static void main(String[] args){//表示定义一个公开的静态的主方法 一个程序的入口
		 /*
		 入口里边 这是方法体 
		 java语句【java语句以;终止，分号必须是半角分号】
		 以下这样代码的作用是向控制台输出一段消息
		 以下的双引号 也必须是半角的【java语法的一部分】
		 java中所有的"字符串"都用这个双引号括起来.
         */

		 System.out.println("Hello World!");/**
		 *doc注释
		
		 *
		 */
		 //再次向控制台输出信息的例子 java语法不能用全角的符号
		 System.out.println("我是火车王！");
	 }
}