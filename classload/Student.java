/*
学生类
学生类是一个模板
描述了所有学生的共同特征【状态+行为】
当前类只描述学生的状态信息【属性】
//stduent是类，属于引用数据类型，这个类型名就是：student

*/
public class Student
{
	//类体-属性+方法
	//属性【存储数据采用变量的形式】
	//由于变量定义在类体当中，方法体之后，这种变量称为成员变量。
	//所有学生都有学号信息
	//但是每一个学生的学号的都是不同的
	//所以要访问后这个学号必须先创建对象，通过对象去访问学号信息
	//学生信心不能直接通过类去访问，所以这种成员变量又被叫做：实例变量
	//对象又被称为实例，实例变量又被称为对象变量。【对象级别的变量】
	//不创建对象，这个No变量的内存空间是不存在的，只有创建了对象，这个No变量内存空间才会创建。

	int No;

	//姓名
	String name;

	//年龄
	int age;

	//性别
	boolean sex;

	//住址
	String addr;



}
//成员变量没有手动赋值的话 系统默认赋予默认值
