/*
 关于java中的"+"运算符：
     1、+运算符在java语言当中有两个作用：
	         *加法运算、求和
			 *字符串的连接运算

	 2、当"+"号运算符两边的数据都是数字的话，一定是进行加法运算。

	 3、当"+"运算符两边的数据只要一个数据是字符串，一定会进行字符串连接运算。
	 并且，连接运算之后的结果还是一个字符串类型。

	    数字+ "数字" -->字符串【字符串连接】
		数字+数字-->数字求和
	 
	 4、一个表达式中可以出现多个+号，没有小括号的前提下，遵循从左向右的顺序依次计算。



*/

